#ifndef INC_API__FATFS_EXFAT_H
#define INC_API__FATFS_EXFAT_H

/**
 * This module implements the Extensible File Allocation Table file system,
 * otherwise known as "exFAT", as created by Microsoft and originally shipped
 * with Windows CE 6.0 in November, 2006.
 *
 * The SANS Institute reverse-engineered the format in 2010, and in 2019
 * Microsoft published a specification of the format publicly to enable its
 * use in projects such as Linux.
 *
 * It is also considered to be a break in the trend of making FAT FATter, as
 * it is sometimes referred to as the "formerly FAT file system".
 *
 * It is worth noting that Microsoft holds U.S. Patent No. 8,321,439 on a
 * feature employed in this format that does not expire until at least 2032.
 */

#endif /* INC_API__FATFS_EXFAT_H */
