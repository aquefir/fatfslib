#ifndef INC_API__FATFS_VFAT_H
#define INC_API__FATFS_VFAT_H

/**
 * This module handles the translation of "Long File Names" which debuted with
 * MS-DOS 7.0 and Microsoft Windows 95. "VFAT" stands for Virtual File
 * Allocation Table and owes its name to the "virtual FAT" driver that shipped
 * with Windows 95.
 *
 * In another sense it is a continuation, like FAT32, of the trend of making
 * FAT FATter, ergo it is "Very FAT".
 *
 * Notably, Microsoft has a history of prosecuting users of this feature due
 * to their holdings of U.S. patents No. 5,579,517, No. 5,745,902,
 * No. 5,758,352, and No. 6,286,013; however, these have all expired as of
 * 2013.
 */

#endif /* INC_API__FATFS_VFAT_H */
